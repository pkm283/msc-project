      Just as C++ was initially intended to be “a better C,” Kotlin was
      initially oriented towards being “a better Java.” It has since evolved
      significantly beyond that goal. Kotlin pragmatically chooses only the most
      successful and helpful features from other programming languages—after
      those features have been field-tested and proven especially valuable.
      Thus, if you are coming from another language, you might recognize some
      features of that language in Kotlin. This is intentional: Kotlin maximizes
      productivity by leveraging tested concepts. Readability is a primary goal
      in the design of the language. Kotlin syntax is concise—it requires no
      ceremony for most scenarios, but can still express complex ideas.
      <b /> Kotlin comes from JetBrains, a company that specializes in developer
      tooling. It has first-class tooling support, and many language features
      were designed with tooling in mind.
